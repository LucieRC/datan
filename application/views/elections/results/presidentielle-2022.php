<h2 class="my-4">Résultats de l'élection présientielle de 2017</h2>
<p>L'élection présidentielle de 2017 s'est déroulée les dimanches 23 avril et 7 mai.</p>
<p>Le président sortant, François Hollande, a décidé de ne pas se représenter. Au total, <b>onze candidats</b> ont été validées par le Conseil constitutionnel.</p>
<p>Au premier tour, <b>Emmanuel Macron</b> (En Marche) arrive en tête avec 24,01 % des voix, suivi de <b>Marine Le Pen</b> (Front national), avec 21,30 % des voix. Ils sont suivis de François Fillon (Les Républicains) et de Jean-Luc Mélenchon (France insoumise). Les deux candidats arrivés en tête (Emmanuel Macron et Marine Le Pen) sont sélectionnés pour le second tour.</p>
<p>Au second tour, <b>Emmanuel Macron</b> est élu président avec 66,10 % des voix.</p>
